    private fun mostrarResultadosPorFecha(simulador: SimuladorTorneo) {
        println("=== RESULTADOS POR JORNADA ===")
        val partidosPorFecha = simulador.obtenerPartidosPorFecha()

        if (partidosPorFecha.isEmpty()) {
            println("No se jugaron partidos")
            return
        }

        partidosPorFecha.forEachIndexed { indexFecha, partidosFecha ->
            println("\nüìÖ JORNADA ${indexFecha + 1}:")
            println("=".repeat(50))
            partidosFecha.forEachIndexed { indexPartido, partido ->
                println("${indexPartido + 1}. $partido")
            }
        }
        println()
    }

    private fun mostrarClasificacionEuropea(tablaPosiciones: Map<String, EstadisticasEquipo>) {
        val tablaOrdenada = tablaPosiciones.entries.sortedWith(
            compareByDescending<Map.Entry<String, EstadisticasEquipo>> { it.value.puntos }
                .thenByDescending { it.value.diferenciaGoles }
                .thenByDescending { it.value.golesFavor }
        )

        println("=== CLASIFICACI√ìN EUROPEA ===")
        println("-".repeat(40))

        if (tablaOrdenada.isNotEmpty()) {
            // Campe√≥n (Champions League)
            println("üèÜ CAMPE√ìN: ${tablaOrdenada[0].key}")
            println("   ${tablaOrdenada[0].value.puntos} puntos | DIF: ${tablaOrdenada[0].value.diferenciaGoles}")
            println("   ‚û§ Clasificado para la Fase de Grupos de la Champions League")
            println()

            // Puestos Champions League (2¬∞ a 4¬∞)
            if (tablaOrdenada.size > 1) {
                println("üá™üá∫ CHAMPIONS LEAGUE:")
                for (i in 1..3) {
                    if (i < tablaOrdenada.size) {
                        println("   ${i + 1}. ${tablaOrdenada[i].key}")
                        println("      ${tablaOrdenada[i].value.puntos} puntos | DIF: ${tablaOrdenada[i].value.diferenciaGoles}")
                    }
                }
                println()
            }

            // Europa League (5¬∞ y 6¬∞)
            if (tablaOrdenada.size > 4) {
                println("üá™üá∫ EUROPA LEAGUE:")
                for (i in 4..5) {
                    if (i < tablaOrdenada.size) {
                        println("   ${i + 1}. ${tablaOrdenada[i].key}")
                        println("      ${tablaOrdenada[i].value.puntos} puntos | DIF: ${tablaOrdenada[i].value.diferenciaGoles}")
                    }
                }
                println()
            }

            // Conference League (7¬∞)
            if (tablaOrdenada.size > 6) {
                println("üá™üá∫ CONFERENCE LEAGUE:")
                println("   7. ${tablaOrdenada[6].key}")
                println("      ${tablaOrdenada[6].value.puntos} puntos | DIF: ${tablaOrdenada[6].value.diferenciaGoles}")
                println()
            }

            // Descenso (√∫ltimos 3)
            if (tablaOrdenada.size >= 3) {
                println("‚¨áÔ∏è DESCENSO:")
                val ultimosTres = tablaOrdenada.takeLast(3)
                ultimosTres.forEachIndexed { index, equipo ->
                    val posReal = tablaOrdenada.size - 2 + index
                    println("   ${posReal + 1}. ${equipo.key}")
                    println("      ${equipo.value.puntos} puntos | DIF: ${equipo.value.diferenciaGoles}")
                }
            }
        }

        println("=".repeat(50))
    }
